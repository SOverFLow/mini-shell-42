#include "../minishell.h"

void    ft_env(t_env *head)
{

    while(head)
	{
		ft_putstr_fd(head->key, 1);
        printf("=");
        ft_putstr_fd(head->val, 1);
        printf("\n");
	}
	return (1);
}
